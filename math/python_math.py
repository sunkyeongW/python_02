#예제1

def add(x, y):
    return x + y

def subtract(x, y):
    return x - y

def multiply(x, y):
    return x * y

def divide(x, y):
    return x / y

def power(x, y):
    return x ** y

if __name__ == "__main__":
    print('-' * 15)
    print(add(5,5))
    print(subtract(5,5))
    print(multiply(5,5))
    print(divide(5,5))
    print(power(5,5))
    print('-' * 15)